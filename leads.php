  <h1>Что такое кредитная история?</h1>
  Кредитная история - это дело о заемщике в котором хранится информация  обо всех взятых
   кредитах заемщика и  своевременного или были погашения. Данная информация
   помогает финансовым компаниям и
   банкам определиться, стоит ли выдавать клиенту займ или нет. </br>

  <h1>Что делать с плохой кредитной историей?</h1>
  Если, так вышло, что ваша кредитная история испорчена, не по вашей вине, а по вине
   банка или другой кредитной организации, вам нужно своевременно написать обращение в
    Украинское бюро кредитных историй (УБКИ ), навести факты, которые подтверждают порчу
     истории со стороны организаций. После обращения, УБКИ, в течении 14 дней обработает
      письмо и вынесет свое решение в письменном виде. Поэтому, испорченная кредитная
      история не по вашей вине – не
   является приговором, и отказами в кредите.</br>
  <h2>Как исправить кредитную историю?</h2>
Исправить кредитную историю по вину долгих просрочек будет очень не просто.
 Для этого нужно доказать кредиторам, вашу финансовую ответственность путем
 подачи заявок на небольшие суммы у разные кредитные компании и своевременного
  их погашения. Данный способ нужен для улучшения
   индекса вашей кредитной истории и посте пары таких обращений организации увидят в
    вас нового человека, которому можно выдавать займы и на большие суммы. Главное
     расценивать ваши силы и ответственно наблюдать за статусами ваших кредитов. </br>
     <h1>Как взять кредит, если нет кредитной истории?</h1>
  К клиентам без кредитной истории банки относятся насторожено,
   обычно для таких клиентов устанавливают лимиты на получения до 3000 грн.
   Поэтому если брать кредит 1 раз, нужно выбирать сумму до 3000грн для увеличения
   шансов на получение кредита. Обычно кредитные компании предоставляют 1 зам под 0%.
   Поэтом. Первый вашей кредит будет отправной точкой, без переплат, к хорошей кредитной
    истории и больших сумм на последующие обращения. Главное закрыть сво1 первый кредит
     без просрочек и задержек по уплате кредита. </br>
